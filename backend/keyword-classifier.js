    classifyKeyword(keyword) {
        const lowerKeyword = keyword.toLowerCase();
        let scores = {};
        
        for (let [category, keywords] of Object.entries(this.categoryKeywords)) {
            let score = 0;
            for (let kw of keywords) {
                if (lowerKeyword.includes(kw)) score += 10;
                if (kw.includes(lowerKeyword) && lowerKeyword.length > 2) score += 5;
            }
            if (score > 0) scores[category] = score;
        }
        
        if (Object.keys(scores).length > 0) {
            return Object.entries(scores).sort((a, b) => b[1] - a[1])[0][0];
        }
        
        if (lowerKeyword.includes('_') || lowerKeyword.includes('-')) {
            return '📚 دانش عمومی (ترکیبی)';
        }
        return '📚 دانش عمومی';
    }

    decomposeCompoundKeyword(keyword) {
        const result = { original: keyword, parts: [], type: 'single', totalParts: 1, hasPersian: false, hasEnglish: false };
        
        if (keyword.includes('_')) {
            result.type = 'underscore';
            result.parts = keyword.split('_');
        } else if (keyword.includes('-')) {
            result.type = 'dash';
            result.parts = keyword.split('-');
        } else if (keyword.includes(' ')) {
            result.type = 'space';
            result.parts = keyword.split(' ');
        } else {
            result.parts = [keyword];
        }
        
        result.totalParts = result.parts.length;
        result.parts = result.parts.map(part => ({
            text: part,
            isPersian: /[\u0600-\u06FF]/.test(part),
            isEnglish: /[a-zA-Z]/.test(part),
            isNumber: /[0-9]/.test(part),
            length: part.length,
            category: this.classifyKeyword(part)
        }));
        
        result.hasPersian = result.parts.some(p => p.isPersian);
        result.hasEnglish = result.parts.some(p => p.isEnglish);
        
        return result;
    }

    classifyAll(keywords) {
        const classified = {};
        for (let category of Object.keys(this.categories)) classified[category] = [];
        for (let keyword of keywords) {
            const category = this.classifyKeyword(keyword);
            const decomposed = this.decomposeCompoundKeyword(keyword);
            if (!classified[category]) classified[category] = [];
            classified[category].push({
                keyword: keyword, decomposed: decomposed, length: keyword.length,
                wordCount: keyword.split(/[_\- ]/).length, partsCount: decomposed.totalParts
            });
        }
        for (let category of Object.keys(classified)) {
            if (classified[category].length === 0) delete classified[category];
        }
        return classified;
    }

    getStatistics(classified) {
        const stats = {};
        let total = 0;
        for (let [category, items] of Object.entries(classified)) {
            stats[category] = {
                count: items.length, percentage: 0, sampleKeywords: items.slice(0, 5).map(i => i.keyword),
                avgLength: Math.round(items.reduce((sum, i) => sum + i.length, 0) / items.length),
                compoundCount: items.filter(i => i.partsCount > 1).length
            };
            total += items.length;
        }
        for (let category of Object.keys(stats)) {
            stats[category].percentage = ((stats[category].count / total) * 100).toFixed(1);
        }
        return { totalKeywords: total, totalCategories: Object.keys(stats).length, categories: stats, timestamp: new Date().toISOString() };
    }

    search(keywords, searchTerm) {
        const lowerSearch = searchTerm.toLowerCase();
        const results = [];
        for (let keyword of keywords) {
            if (keyword.toLowerCase().includes(lowerSearch)) {
                results.push({ keyword: keyword, matchType: 'contains', category: this.classifyKeyword(keyword) });
            }
        }
        return { searchTerm: searchTerm, results: results, totalFound: results.length };
    }
